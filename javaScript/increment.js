// increment operator 
let num1=50;
 num1++
 num1++                     // post increment
 num1++                      // ans=53;
 console.log(num1)
//  -------------------------------------------------------
let num2=50;
num2--;                      //post decrement 
num2--;                      // ans=48;
console.log(num2);
// ------------------------------------------------------
let num3=50;
let ans=--num3;                //prefix decrement 
console.log(ans)              //ans=49
// -------------------------------------------------
let  num4=10;
let result=++num4;             // prefix addition
console.log(result)           // ans=11;
// -----------------------------------------------------



